public class even_odd {
    public static void main(String[] args){
            int n=20;
            int i;
            for (i=1;i<=n;i++){
                if(i%2==0){
                    System.out.println(i +" even number");
                }
                else{
                    System.out.println(i+" odd number");
                }

            }
    }
}
